let randomNumber = 69 // Assignment

// Addition & re-assignment
randomNumber += 42 // randomNumber = randomNumber + 42

// Substraction & re-assignment
randomNumber -= 42 // randomNumber = randomNumber - 42

// Multiplication & re-assignment
randomNumber *= 2 // randomNumber = randomNumber * 2

// Division & re-assignment
randomNumber /= 2 // randomNumber = randomNumber / 2

// Modulo & re-assignment
randomNumber %= 5 // randomNumber = randomNumber % 5

// Increment
randomNumber++ // Postfix
++randomNumber // Prefix

// Decrement
randomNumber--
--randomNumber